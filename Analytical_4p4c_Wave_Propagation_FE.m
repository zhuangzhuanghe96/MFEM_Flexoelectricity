%% Local functions
function detM = compute_M(frequency, kValue)
    global c11 c13 c33 c44 l mu11 mu12 mu44 ep11 rho
    global h a

    omega = 2 * pi * frequency;
    k1 = (pi / a) * kValue;
    k3p = getK3(frequency, k1);
    boundaryMatrix = zeros(10, 10);

    % Cache powers that are reused in each boundary-condition column.
    l2 = l^2;
    k1_2 = k1^2;
    k1_3 = k1^3;
    k1_4 = k1^4;
    rho_w2 = rho * omega^2;

    for columnIndex = 1:10
        k3 = k3p(columnIndex);
        k3_2 = k3^2;
        k3_3 = k3^3;
        k3_4 = k3^4;

        % Assemble the coupled mechanical-electric coefficients.
        k11 = -c11*k1_2 - c44*k3_2 + rho_w2 ...
            - l2*c11*k1_4 - l2*(c11 + c44)*k1_2*k3_2 - l2*c44*k3_4;
        k12 = -(c13 + c44)*k1*k3 ...
            - l2*(c13 + c44)*k1_3*k3 - l2*(c13 + c44)*k1*k3_3;
        k13 = 1i*mu11*k1_3 + 1i*(mu12 + 2*mu44)*k1*k3_2;

        k22 = -c44*k1_2 - c33*k3_2 + rho_w2 ...
            - l2*(c33 + c44)*k1_2*k3_2 - l2*c33*k3_4 - l2*c44*k1_4;
        k23 = 1i*mu11*k3_3 + 1i*(mu12 + 2*mu44)*k1_2*k3;

        bj = -(k23*k11 - k13*k12) / (k23*k12 - k13*k22);
        cj = -(k22*k11 - k12*k12) / (k22*k13 - k12*k23);

        % Evaluate the ten boundary-condition terms for this k3 branch.
        term12 = c44*1i*k3 + c44*1i*k1*bj ...
            + l2*c44*(1i*k1_2*k3 + 1i*k1_3*bj) ...
            + l2*c44*(1i*k3_3 + 1i*k1*k3_2*bj) ...
            + 2*mu44*k1*k3*cj + l2*c11*1i*k1_2*k3 ...
            + l2*c13*1i*k1*k3_2*bj + mu12*k1*k3*cj;

        term34 = c13*1i*k1 + c33*1i*k3*bj ...
            + l2*c13*1i*k1_3 + l2*c33*1i*k1_2*k3*bj ...
            + mu12*k1_2*cj + l2*c13*1i*k1*k3_2 ...
            + l2*c33*1i*k3_3*bj + mu11*k3_2*cj ...
            + l2*c44*(1i*k1*k3_2 + 1i*k1_2*k3*bj) + mu44*k1_2*cj;

        term56 = -l2*c44*(k3_2 + k1*k3*bj) + mu44*1i*k1*cj;
        term78 = -l2*c13*k1*k3 - l2*c33*k3_2*bj + mu11*1i*k3*cj;
        term910 = -mu44*k1*k3 - mu44*k1_2*bj - mu12*k1*k3 ...
            - mu11*k3_2*bj - ep11*1i*k3*cj;

        % Apply the top and bottom surface phase factors.
        expTop = exp(1i * k3 * (h / 2));
        expBottom = exp(1i * k3 * (-h / 2));

        boundaryMatrix(1, columnIndex) = term12 * expTop;
        boundaryMatrix(2, columnIndex) = term12 * expBottom;
        boundaryMatrix(3, columnIndex) = term34 * expTop;
        boundaryMatrix(4, columnIndex) = term34 * expBottom;
        boundaryMatrix(5, columnIndex) = term56 * expTop;
        boundaryMatrix(6, columnIndex) = term56 * expBottom;
        boundaryMatrix(7, columnIndex) = term78 * expTop;
        boundaryMatrix(8, columnIndex) = term78 * expBottom;
        boundaryMatrix(9, columnIndex) = term910 * expTop;
        boundaryMatrix(10, columnIndex) = term910 * expBottom;
    end

    detM = abs(det(boundaryMatrix));

    % Keep the optimizer stable when the determinant overflows.
    if isinf(detM) || isnan(detM)
        detM = realmax;
    end
end

function result = Searoots(fun, xStart, xStep, xEnd, kValue)
    % First locate coarse local minima, then refine each candidate interval.
    xNodes = xStart:xStep:xEnd;
    yNodes = zeros(1, numel(xNodes));

    for nodeIndex = 1:numel(xNodes)
        yNodes(nodeIndex) = fun(xNodes(nodeIndex), kValue);
    end

    localMinMask = (yNodes(2:end-1) < yNodes(1:end-2)) ...
        & (yNodes(2:end-1) < yNodes(3:end));
    localMinIndices = find(localMinMask) + 1;

    result = [];
    options = optimset('Display', 'off', 'TolX', 1e-3);

    for nodeIndex = localMinIndices
        lowerBound = xNodes(nodeIndex - 1);
        upperBound = xNodes(nodeIndex + 1);

        [frequencyOpt, determinantMin] = fminbnd( ...
            @(frequency) fun(frequency, kValue), lowerBound, upperBound, options);

        % Accept only sharp determinant valleys that behave like true roots.
        if determinantMin < yNodes(nodeIndex) * 1e-2 || determinantMin < 1e-10
            result = [result; frequencyOpt, determinantMin];
        end
    end
end

function k3p = getK3(frequency, k1)
    global c11 c13 c33 c44 l mu11 mu12 mu44 ep11 ep11 rho

    omega = 2 * pi * frequency;

    % Precompute powers for the polynomial coefficients in k3^2.
    l2 = l^2;
    l4 = l^4;
    k1_2 = k1^2;
    k1_4 = k1^4;
    k1_6 = k1^6;
    k1_8 = k1^8;
    k1_10 = k1^10;
    w2rho = omega^2 * rho;
    w4rho2 = w2rho^2;

    P1 = c33*c44*ep11*l4 + c44*l2*mu11^2;

    P2 = 2*c33*c44*ep11*k1_2*l4 - 2*c13*c44*ep11*k1_2*l4 ...
        + c33*c44*ep11*k1_2*l4 + c11*c33*ep11*k1_2*l4 ...
        + 4*c33*k1_2*l2*mu12*mu44 - 4*c13*k1_2*l2*mu11*mu44 ...
        - 2*c13*k1_2*l2*mu11*mu12 + 4*c33*k1_2*l2*mu44^2 ...
        + c44*k1_2*l2*mu11^2 + c33*k1_2*l2*mu12^2 ...
        + c11*k1_2*l2*mu11^2 + 2*c33*c44*ep11*l2 ...
        - c13^2*ep11*k1_2*l4 + c44*mu11^2;

    P3 = c33*c44*ep11*k1_4*l4 + 2*c33*c44*ep11*k1_2*l2 ...
        - 4*c13*c44*ep11*k1_4*l4 + 2*c33*c44*ep11*k1_4*l4 ...
        - 4*c13*c44*ep11*k1_2*l2 + c11*c44*ep11*k1_4*l4 ...
        + 2*c33*c44*ep11*k1_2*l2 + 2*c11*c33*ep11*k1_4*l4 ...
        + 2*c11*c33*ep11*k1_2*l2 - 2*c13*c44*ep11*k1_4*l4 ...
        + c11*c33*ep11*k1_4*l4 + 4*c33*k1_4*l2*mu12*mu44 ...
        + 4*c33*k1_4*l2*mu11*mu44 - 8*c13*k1_4*l2*mu12*mu44 ...
        - 4*c13*k1_4*l2*mu11*mu44 + 4*c11*k1_4*l2*mu11*mu44 ...
        + 2*c33*k1_4*l2*mu11*mu12 - 2*c13*k1_4*l2*mu11*mu12 ...
        + 2*c11*k1_4*l2*mu11*mu12 + 4*c33*k1_4*l2*mu44^2 ...
        - 8*c13*k1_4*l2*mu44^2 - 2*c44*k1_4*l2*mu11^2 ...
        + c33*k1_4*l2*mu12^2 - 2*c13*k1_4*l2*mu12^2 ...
        - 2*c13*k1_4*l2*mu11^2 + c11*k1_4*l2*mu11^2 ...
        - 2*c13^2*ep11*k1_4*l4 - 2*c13^2*ep11*k1_2*l2 ...
        + 4*c33*k1_2*mu12*mu44 - 4*c13*k1_2*mu11*mu44 ...
        - 2*c13*k1_2*mu11*mu12 - c44*ep11*l2*w2rho ...
        - c33*ep11*l2*w2rho - c13^2*ep11*k1_4*l4 ...
        + c33*c44*ep11 + 4*c33*k1_2*mu44^2 ...
        + c33*k1_2*mu12^2 + c11*k1_2*mu11^2 - mu11^2*w2rho;

    P4 = -2*c44*ep11*k1_2*l2*w2rho - 2*c13*c44*ep11*k1_6*l4 ...
        - 4*c13*c44*ep11*k1_4*l2 + 2*c11*c44*ep11*k1_6*l4 ...
        + c33*c44*ep11*k1_6*l4 + 2*c33*c44*ep11*k1_4*l2 ...
        + 2*c11*c44*ep11*k1_4*l2 + c11*c33*ep11*k1_6*l4 ...
        + 2*c11*c33*ep11*k1_4*l2 - 4*c13*c44*ep11*k1_6*l4 ...
        - 4*c13*c44*ep11*k1_4*l2 + c11*c44*ep11*k1_6*l4 ...
        + 2*c11*c33*ep11*k1_6*l4 + 2*c11*c33*ep11*k1_4*l2 ...
        - c33*ep11*k1_2*l2*w2rho - c44*ep11*k1_2*l2*w2rho ...
        - c33*ep11*k1_2*l2*w2rho - c11*ep11*k1_2*l2*w2rho ...
        - 4*k1_2*mu12*mu44*w2rho - 4*k1_2*mu11*mu44*w2rho ...
        - 2*k1_2*mu11*mu12*w2rho + 4*c33*k1_6*l2*mu11*mu44 ...
        - 8*c13*k1_6*l2*mu12*mu44 - 4*c13*k1_6*l2*mu11*mu44 ...
        + 4*c11*k1_6*l2*mu12*mu44 + 4*c11*k1_6*l2*mu11*mu44 ...
        + 2*c33*k1_6*l2*mu11*mu12 - 2*c13*k1_6*l2*mu11*mu12 ...
        + 2*c11*k1_6*l2*mu11*mu12 - 8*c13*k1_6*l2*mu44^2 ...
        + 4*c11*k1_6*l2*mu44^2 - 2*c44*k1_6*l2*mu11^2 ...
        + c33*k1_6*l2*mu11^2 - 2*c13*k1_6*l2*mu12^2 ...
        - 2*c13*k1_6*l2*mu11^2 + c11*k1_6*l2*mu12^2 ...
        - 2*c13^2*ep11*k1_4*l2 - 2*c13^2*ep11*k1_6*l4 ...
        - 2*c13^2*ep11*k1_4*l2 + 4*c33*k1_4*mu11*mu44 ...
        - 8*c13*k1_4*mu12*mu44 + 4*c11*k1_4*mu11*mu44 ...
        + 2*c33*k1_4*mu11*mu12 + 2*c11*k1_4*mu11*mu12 ...
        - 2*c13*c44*ep11*k1_2 + c33*c44*ep11*k1_2 ...
        + c11*c33*ep11*k1_2 - 4*k1_2*mu44^2*w2rho ...
        - c13^2*ep11*k1_6*l4 - c44*ep11*w2rho - c33*ep11*w2rho ...
        - 8*c13*k1_4*mu44^2 - 2*c44*k1_4*mu11^2 ...
        - 2*c13*k1_4*mu12^2 - 2*c13*k1_4*mu11^2 ...
        - k1_2*mu12^2*w2rho - c13^2*ep11*k1_2;

    P5 = -2*c44*ep11*k1_4*l2*w2rho - 4*k1_4*mu12*mu44*w2rho ...
        - 4*k1_4*mu11*mu44*w2rho - 2*k1_4*mu11*mu12*w2rho ...
        - 4*c13*k1_8*l2*mu11*mu44 + 4*c11*k1_8*l2*mu12*mu44 ...
        - 2*c13*k1_8*l2*mu11*mu12 + c11*c44*ep11*k1_8*l4 ...
        + 2*c11*c44*ep11*k1_6*l2 - 2*c13*c44*ep11*k1_8*l4 ...
        - 4*c13*c44*ep11*k1_6*l2 + 2*c11*c44*ep11*k1_8*l4 ...
        + 2*c11*c44*ep11*k1_6*l2 + c11*c33*ep11*k1_8*l4 ...
        + 2*c11*c33*ep11*k1_6*l2 - c44*ep11*k1_4*l2*w2rho ...
        - c33*ep11*k1_4*l2*w2rho - c11*ep11*k1_4*l2*w2rho ...
        - c11*ep11*k1_4*l2*w2rho - 4*c13*k1_6*mu11*mu44 ...
        + 4*c11*k1_6*mu12*mu44 - 2*c13*k1_6*mu11*mu12 ...
        + c11*c44*ep11*k1_4 - 2*c13*c44*ep11*k1_4 ...
        + c11*c33*ep11*k1_4 - 4*k1_4*mu44^2*w2rho ...
        - c44*ep11*k1_2*w2rho - c44*ep11*k1_2*w2rho ...
        - c33*ep11*k1_2*w2rho - c11*ep11*k1_2*w2rho ...
        + 4*c11*k1_8*l2*mu44^2 + c44*k1_8*l2*mu11^2 ...
        + c33*k1_8*l2*mu11^2 + c11*k1_8*l2*mu12^2 ...
        - 2*c13^2*ep11*k1_6*l2 + ep11*w4rho2 ...
        + 4*c11*k1_6*mu44^2 + c33*k1_6*mu11^2 ...
        + c11*k1_6*mu12^2 - k1_4*mu12^2*w2rho ...
        - c13^2*ep11*k1_8*l4 - c13^2*ep11*k1_4;

    P6 = -c44*ep11*k1_6*l2*w2rho - c11*ep11*k1_6*l2*w2rho ...
        + c11*c44*ep11*k1_10*l4 + 2*c11*c44*ep11*k1_8*l2 ...
        + c11*c44*ep11*k1_6 - c44*ep11*k1_4*w2rho ...
        - c11*ep11*k1_4*w2rho + ep11*k1_2*w4rho2 ...
        + c44*k1_10*l2*mu11^2 + c44*k1_8*mu11^2 ...
        - k1_6*mu11^2*w2rho;

    k3SquaredRoots = roots([P1, P2, P3, P4, P5, P6]);
    k3p = sqrt(k3SquaredRoots);
    k3p = [k3p; -k3p];
end
