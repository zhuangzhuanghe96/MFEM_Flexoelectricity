clc; clear; close all;

%% Geometry and material parameters
% These parameters are global because the local functions evaluate the same
% dispersion equations repeatedly during the frequency sweep.
global h a
global c11 c13 c33 c44 l mu11 mu12 mu44 ep11 rho

h = 5e-9;
a = 2 * h;

c11 = 173e9;
c13 = 82e9;
c33 = 173e9;
c44 = 108e9;

l = h / 5;

mu11 = 0.15e-9;
mu12 = 100e-9;
mu44 = -1.9e-9;

ep11 = 13.28e-9;

rho = 5996;

%% Optional comparison data
comparisonFile = 'MFEM_Flexo_DispersionCurve.txt';

if exist(comparisonFile, 'file') == 2
    femData = readmatrix(comparisonFile);
    plot(femData(:, 1), femData(:, 2:end), '.', ...
        'MarkerSize', 10, 'Color', [0.85 0.85 0.85]);
    hold on;
end

%% Dispersion sweep
waveNumbers = [0.001, 0.02:0.02:1];
frequencyStart = 0.1e8;
frequencyStep = 1e9 / 4;
frequencyEnd = 7.3e11;
data = [];

for rowIndex = 1:numel(waveNumbers)
    kValue = waveNumbers(rowIndex);
    tic;
    fprintf('Computing k = %.4g\n', kValue);

    rootCandidates = Searoots(@compute_M, frequencyStart, frequencyStep, frequencyEnd, kValue);

    if ~isempty(rootCandidates)
        plot(kValue * ones(size(rootCandidates(:, 1))), rootCandidates(:, 1), 'ro', ...
            'MarkerSize', 2, 'MarkerFaceColor', 'r');
        hold on;
        drawnow;

        data(rowIndex, 1:(size(rootCandidates, 1) + 1)) = [kValue, rootCandidates(:, 1).'];
    else
        data(rowIndex, 1) = kValue;
    end

    toc;
end


title('Flexoelectric Wave Dispersion');
xlim([0, 1]);
xlabel('Wave number (\pi/(2h))');
ylabel('Frequency (Hz)');
